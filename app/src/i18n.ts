export type AppLanguage = 'en' | 'de'

export const supportedLanguages: Array<{ code: AppLanguage; label: string; shortLabel: string }> = [
  { code: 'en', label: 'English', shortLabel: 'EN' },
  { code: 'de', label: 'Deutsch', shortLabel: 'DE' },
]

const languageStorageKey = 'open-cowork.language'

function normalizeLanguage(value: string | null | undefined): AppLanguage | null {
  const normalized = value?.toLowerCase().split('-')[0]
  return normalized === 'de' || normalized === 'en' ? normalized : null
}

function getInitialLanguage(): AppLanguage {
  if (typeof window !== 'undefined') {
    try {
      return normalizeLanguage(window.localStorage.getItem(languageStorageKey)) ?? 'en'
    } catch {
      return 'en'
    }
  }
  return 'en'
}

